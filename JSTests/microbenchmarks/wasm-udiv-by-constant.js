// Microbenchmark for 32-bit unsigned division by constants with 33-bit magic multipliers.
// Divisors 7, 19, and 107 all trigger the magic.add path in B3's strength reduction,
// exercising the UMulHigh64 optimization from Mitsunari & Hoshino (2026).
//
// WASM module source (WAT):
//
// (module
//   (func (export "test") (param $ret i32) (result i32)
//     (local $i i32)
//     (local $tmp i32)
//     (loop $loop
//       (local.set $tmp (i32.xor (local.get $i) (local.get $ret)))
//       (local.set $ret (i32.xor (local.get $ret) (i32.div_u (local.get $tmp) (i32.const 7))))
//       (local.set $tmp (i32.xor (local.get $i) (local.get $ret)))
//       (local.set $ret (i32.xor (local.get $ret) (i32.div_u (local.get $tmp) (i32.const 19))))
//       (local.set $tmp (i32.xor (local.get $i) (local.get $ret)))
//       (local.set $ret (i32.xor (local.get $ret) (i32.div_u (local.get $tmp) (i32.const 107))))
//       (local.set $i (i32.add (local.get $i) (i32.const 1)))
//       (br_if $loop (i32.lt_u (local.get $i) (i32.const 1000000))))
//     (local.get $ret)))

