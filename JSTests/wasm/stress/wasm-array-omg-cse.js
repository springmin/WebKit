// ─── 1–4. Mutable i32 CSE ─────────────────────────────────────────────────────
{
    const { dupGetConst, dupGetDyn, deadStore, storeLoadFwd, alloc } = makeInstance(CSE).exports;
    const arr = alloc(16);

    // 1. Two identical constant-index gets: result = val + val
    for (let i = 0; i < N; i++) {
        const v = i % 5000;
        const r = dupGetConst(arr, v);
        if (r !== v * 2)
            throw new Error(`dupGetConst(${v}): expected ${v * 2}, got ${r}`);
    }

    // 2. Two identical dynamic-index gets: result = val + val
    for (let i = 0; i < N; i++) {
        const idx = i % 16;
        const v = i % 5000;
        const r = dupGetDyn(arr, idx, v);
        if (r !== v * 2)
            throw new Error(`dupGetDyn(idx=${idx}, val=${v}): expected ${v * 2}, got ${r}`);
    }

    // 3. Dead store: first write (999) is clobbered, second (val) wins
    for (let i = 0; i < N; i++) {
        const v = i % 5000;
        const r = deadStore(arr, v);
        if (r !== v)
            throw new Error(`deadStore(${v}): expected ${v}, got ${r}`);
    }

    // 4. Store-to-load forwarding: get must return the just-stored value
    for (let i = 0; i < N; i++) {
        const v = i % 5000;
        const r = storeLoadFwd(arr, v);
        if (r !== v)
            throw new Error(`storeLoadFwd(${v}): expected ${v}, got ${r}`);
    }
}

// ─── 5. Immutable array CSE ───────────────────────────────────────────────────
{
    const { addAt, alloc } = makeInstance(IMM).exports;

    // All elements = 7: addAt = 7 * 7 = 49 (CSE can reuse the single load)
    const arr = alloc(16, 7);
    for (let i = 0; i < N; i++) {
        const r = addAt(arr, i % 16);
        if (r !== 49)
            throw new Error(`immut addAt(7): expected 49, got ${r}`);
    }

    // Sweep several element values
    const vals = [0, 1, 5, 100, 1000];
    for (const v of vals) {
        const a = alloc(16, v);
        for (let i = 0; i < N / vals.length; i++) {
            const r = addAt(a, i % 16);
            if (r !== v * v)
                throw new Error(`immut addAt(${v}): expected ${v * v}, got ${r}`);
        }
    }
}

// ─── 6. i8 store-to-load forwarding with masking ─────────────────────────────
{
    const { fwdU, fwdS, alloc } = makeInstance(I8).exports;
    const arr = alloc(16);

    // raw byte value → expected get_u (zero-extended), expected get_s (sign-extended)
    const CASES = [
        [0,   0,    0],
        [127, 127,  127],
        [128, 128, -128],
        [200, 200,  -56],
        [255, 255,   -1],
    ];

    for (let i = 0; i < N; i++) {
        const [raw, expectU, expectS] = CASES[i % CASES.length];
        const idx = i % 16;

        const u = fwdU(arr, idx, raw);
        if (u !== expectU)
            throw new Error(`i8 fwdU(${raw}): expected ${expectU}, got ${u}`);

        const s = fwdS(arr, idx, raw);
        if (s !== expectS)
            throw new Error(`i8 fwdS(${raw}): expected ${expectS}, got ${s}`);
    }
}
