// Cross-module isorecursive RTT identity:
//
// Two independently-compiled modules declare structurally identical
// recursion groups (a struct with a ref to itself). V8-style isorecursive
// canonicalization must resolve them to the *same* canonical RTT pointer so
// that a value produced by module A can be ref.cast back to module B's
// declared type without failing. See:
// Source/JavaScriptCore/wasm/WasmTypeDefinition.cpp:
// canonicalizeRecursionGroupImpl.

/*
(module
  (rec
    (type $node (struct (field (ref null $node)) (field (mut i32)))))
  (func (export "makeLeaf") (param i32) (result (ref null $node))
    ref.null $node local.get 0 struct.new $node)
  (func (export "leafVal") (param (ref null $node)) (result i32)
    local.get 0 struct.get $node 1))
*/
const M_A = new Uint8Array([
    0x00,0x61,0x73,0x6d,0x01,0x00,0x00,0x00,0x01,0x94,0x80,0x80,0x80,0x00,0x03,0x5f,
    0x02,0x63,0x00,0x00,0x7f,0x01,0x60,0x01,0x7f,0x01,0x63,0x00,0x60,0x01,0x63,0x00,
    0x01,0x7f,0x03,0x83,0x80,0x80,0x80,0x00,0x02,0x01,0x02,0x07,0x96,0x80,0x80,0x80,
    0x00,0x02,0x08,0x6d,0x61,0x6b,0x65,0x4c,0x65,0x61,0x66,0x00,0x00,0x07,0x6c,0x65,
    0x61,0x66,0x56,0x61,0x6c,0x00,0x01,0x0a,0x9c,0x80,0x80,0x80,0x00,0x02,0x89,0x80,
    0x80,0x80,0x00,0x00,0xd0,0x00,0x20,0x00,0xfb,0x00,0x00,0x0b,0x88,0x80,0x80,0x80,
    0x00,0x00,0x20,0x00,0xfb,0x02,0x00,0x01,0x0b,
]);

/*
(module
  (rec
    (type $node (struct (field (ref null $node)) (field (mut i32)))))
  (func (export "castAndRead") (param anyref) (result i32)
    local.get 0 ref.cast (ref null $node) struct.get $node 1))
*/
const M_B = new Uint8Array([
    0x00,0x61,0x73,0x6d,0x01,0x00,0x00,0x00,0x01,0x8d,0x80,0x80,0x80,0x00,0x02,0x5f,
    0x02,0x63,0x00,0x00,0x7f,0x01,0x60,0x01,0x6e,0x01,0x7f,0x03,0x82,0x80,0x80,0x80,
    0x00,0x01,0x01,0x07,0x8f,0x80,0x80,0x80,0x00,0x01,0x0b,0x63,0x61,0x73,0x74,0x41,
    0x6e,0x64,0x52,0x65,0x61,0x64,0x00,0x00,0x0a,0x91,0x80,0x80,0x80,0x00,0x01,0x8b,
    0x80,0x80,0x80,0x00,0x00,0x20,0x00,0xfb,0x17,0x00,0xfb,0x02,0x00,0x01,0x0b,
]);

const { makeLeaf, leafVal } = new WebAssembly.Instance(new WebAssembly.Module(M_A)).exports;
const { castAndRead } = new WebAssembly.Instance(new WebAssembly.Module(M_B)).exports;

for (let i = 0; i < wasmTestLoopCount; i++) {
    const v = (i * 7) | 0;
    const leaf = makeLeaf(v);

    // Round-trip through A's own accessor.
    const a = leafVal(leaf);
    if (a !== v)
        throw new Error(`A leafVal(${v}): expected ${v}, got ${a}`);

    // Cross-module ref.cast + read. If canonicalization is per-module
    // rather than isorecursive-per-recgroup, this ref.cast would trap.
    const b = castAndRead(leaf);
    if (b !== v)
        throw new Error(`B castAndRead(${v}): expected ${v}, got ${b}`);
}
