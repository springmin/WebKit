const result = await WebAssembly.instantiate(wasmBytes);
const {
    test_q15mulr_basic,
    test_q15mulr_lane1,
    test_q15mulr_lane2,
} = result.instance.exports;

for (let i = 0; i < wasmTestLoopCount; ++i) {
    // Lane 0: (0x4000 * 0x4000) >> 15 with rounding = 0x2000 (8192)
    assert.eq(test_q15mulr_basic(), 0x2000);
    // Lane 1: (0x4000 * 0x8000) >> 15 with rounding
    // 0x4000 = 16384, 0x8000 = -32768 (signed)
    // 16384 * -32768 = -536870912 = 0xe0000000
    // >> 15 with rounding = -16384 = 0xc000 (-16384)
    assert.eq(test_q15mulr_lane1(), -16384);
    // Lane 2: (0x7fff * 0x7fff) >> 15 with rounding
    // 0x7fff = 32767 (max signed 16-bit)
    // 32767 * 32767 = 1073676289 = 0x3FFF0001
    // >> 15 with rounding = 32766 (0x7ffe)
    assert.eq(test_q15mulr_lane2(), 0x7ffe);
}
