// ─── i8 tests ─────────────────────────────────────────────────────────────────
{
    const { getU, getS, setAt, alloc } = makeInstance(I8).exports;
    const arr = alloc(16);

    // raw byte value → (expected_u, expected_s)
    const CASES = [
        [0,   0,    0],
        [1,   1,    1],
        [127, 127,  127],
        [128, 128, -128],
        [200, 200,  -56],
        [255, 255,   -1],
    ];

    for (let i = 0; i < N; i++) {
        const [raw, expectU, expectS] = CASES[i % CASES.length];
        const idx = i % 16;

        setAt(arr, idx, raw);

        const u = getU(arr, idx);
        if (u !== expectU)
            throw new Error(`i8 getU(raw=${raw}): expected ${expectU}, got ${u}`);

        const s = getS(arr, idx);
        if (s !== expectS)
            throw new Error(`i8 getS(raw=${raw}): expected ${expectS}, got ${s}`);
    }
}

