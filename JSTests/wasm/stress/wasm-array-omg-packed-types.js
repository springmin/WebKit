// ─── Out-of-bounds / null trap ────────────────────────────────────────────────
{
    const { get, set, alloc } = makeInstance(BOUNDS).exports;
    const arr = alloc(5);

    // Warm up in-bounds (triggers OMG).
    for (let i = 0; i < N; i++) {
        const r = get(arr, i % 5);
        if (r !== 7)
            throw new Error(`bounds in-bounds: expected 7, got ${r}`);
    }

    function mustThrow(fn, label) {
        let threw = false;
        try { fn(); } catch (e) {
            if (!(e instanceof WebAssembly.RuntimeError))
                throw new Error(`${label}: expected RuntimeError, got ${e}`);
            threw = true;
        }
        if (!threw) throw new Error(`${label}: expected exception but none thrown`);
    }

    // OOB get must trap.
    mustThrow(() => get(arr, 5), "OOB get[5]");
    mustThrow(() => get(arr, -1), "OOB get[-1]");

    // OOB set must trap.
    mustThrow(() => set(arr, 5, 0), "OOB set[5]");

    // Null reference must trap.
    mustThrow(() => get(null, 0), "null get");
    mustThrow(() => set(null, 0, 0), "null set");
}
