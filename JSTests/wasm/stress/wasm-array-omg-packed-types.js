// Tests WasmArrayGet / WasmArraySet B3 nodes for packed element types (i8, i16)
// under OMG compilation.  Exercises:
//   - i8 zero-extension (array.get_u) and sign-extension (array.get_s)
//   - i16 zero-extension and sign-extension
//   - Boundary values: 0, 127/32767, 128/32768, 200/50000, 255/65535
//   - Dynamic index sweeps
//   - Out-of-bounds access must trap
//   - Null reference access must trap
//
// Approach: allocate arrays once, loop N times over get/set so OMG fires.

const N = 100000; // enough to trigger OMG (default threshold: 50 000)

